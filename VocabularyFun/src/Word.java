
public class Word {
	
	String aWord, definition;
	
	public Word(String aWord, String definition)
	{	
		this.aWord = aWord;
		this.definition = definition;	
	}

	public String getWord()
	{
		return this.aWord;
	}
	
	public String getDefinition()
	{
		return this.definition;
	}
	
	
	
}//end class
